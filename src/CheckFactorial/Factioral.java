package CheckFactorial;

import java.util.Scanner;

public class Factioral {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int factorial = 1;
		for(int i=0; i<=n; i++) {
			factorial = factorial*i;
		}
		System.out.println(factorial);
		sc.close();

	}

}

























































































































































